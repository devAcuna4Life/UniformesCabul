    private void mostrarDatos() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("NoUniforme");
        modelo.addColumn("Equipo");
        modelo.addColumn("Modelo"); 
        modelo.addColumn("Color");  
        modelo.addColumn("Tallas"); 
        modelo.addColumn("Jersey");
        modelo.addColumn("Accesorios");
        modelo.addColumn("Existencia");
        modelo.addColumn("Precio");
        jTable1.setModel(modelo);

        String consultasql = "SELECT * FROM inventario";
        String data[] = new String[9]; 

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(consultasql);

            while (rs.next()) {
                data[0] = rs.getString("NoUniforme");
                data[1] = rs.getString("Equipo");
                data[2] = rs.getString("Modelo"); 
                data[3] = rs.getString("Color");  
                data[4] = rs.getString("Tallas"); 
                data[5] = rs.getString("Jersey");
                data[6] = rs.getString("Accesorios");
                data[7] = rs.getString("Existencia");
                data[8] = String.valueOf(rs.getDouble("Precio"));

                modelo.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println("Error: " + e);
        }
    }

    public DefaultTableModel obtenerDatosUniformes() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("NoUniforme");
        modelo.addColumn("Equipo");
        modelo.addColumn("Modelo");
        modelo.addColumn("Color");
        modelo.addColumn("Tallas");
        modelo.addColumn("Jersey");
        modelo.addColumn("Accesorios");
        modelo.addColumn("Existencia");
        modelo.addColumn("Precio");

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM inventario");

            while (rs.next()) {
                Object[] data = {
                    rs.getString("NoUniforme"),
                    rs.getString("Equipo"),
                    rs.getString("Modelo"),
                    rs.getString("Color"),
                    rs.getString("Tallas"),
                    rs.getString("Jersey"),
                    rs.getString("Accesorios"),
                    rs.getString("Existencia"),
                    rs.getString("Precio")
                };
                modelo.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println("Error al obtener datos de uniformes: " + e);
        }

        return modelo;
    }

